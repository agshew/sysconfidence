mv -f config.h config.old 2>/dev/null

case ${1} in
	x86cluster)
		echo "/* Generated by config.sh */"		>  config.h
		echo "#ifndef HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define TIMER_X86_64"			>> config.h
		echo "#define NODEID_GETHOSTNAME"		>> config.h
		echo "#endif"                 			>> config.h
		: ;;
	crayxt)
		echo "/* Generated by config.sh */" 		>  config.h
		echo "#ifndef HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define TIMER_X86_64"			>> config.h
		echo "#define NODEID_MPI"			>> config.h
		echo "#endif"					>> config.h
		: ;;
	ibmbgp)
		echo "/* Generated by config.sh */"		>  config.h
		echo "#ifndef HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define TIMER_MPI_WTIME"			>> config.h
		echo "#define NODEID_MPI"			>> config.h
		echo "#endif"					>> config.h
		: ;;
	sicortex)
		echo "/* Generated by config.sh */"		>  config.h
		echo "#ifndef HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define TIMER_MPI_WTIME"			>> config.h
		echo "#define NODEID_SLURM"			>> config.h
		echo "#endif"					>> config.h
		: ;;
	power7)
		echo "/* Generated by config.sh */"		>  config.h
		echo "#ifndef HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define HAVE_CONFIDENCE_CONFIG"		>> config.h
		echo "#define TIMER_PPC64"			>> config.h
		echo "#define NODEID_MPI"			>> config.h
		echo "#endif"					>> config.h
		: ;;
	*)
		echo "Usage: $0 <target>"
		echo "  valid values for target are:"
		echo "    x86cluster -- generic x86-64 (x86-64 timers and gethostname node-ids)"
		echo "    crayxt     -- Cray XT        (x86-64 timers and MPI node-ids)"
		echo "    ibmbgp     -- IBM BG/P       (MPI timers and MPI node-ids)"
		echo "    sicortex   -- SiCortex       (MPI timers and SLURM node-ids)"
		echo "    power7     -- IBM Power7     (MPI timers and MPI node-ids)"
		: ;;
esac
	
